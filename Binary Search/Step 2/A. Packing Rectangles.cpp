void solve() {
    ll w, h, n;
    cin >> w >> h >> n;
    ll l = max(w, h) - 1, r = max(max(w, h), min(w, h) * n) + 1;
    while (r - l > 1) {
        ll m = l + (r - l)/2;
        if (1LL * (m/w) * (m/h) >= n)
            r = m;
        else
            l = m;
    }
    cout << r << nline;
}

