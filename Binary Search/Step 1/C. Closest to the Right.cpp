void solve() {
    int n, k; cin >> n >> k;
    int* a = new int[n];
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < k; i++) {
        int x; cin >> x;
        int l = -1, r = n;
        while (r - l > 1) {
            int m = l + (r - l)/2;
            if (a[m] >= x)
                r = m;
            else
                l = m;
        }
        cout << r + 1 << nline;
    }
}

